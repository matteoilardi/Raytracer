#include "random.hpp"
#include <gtest/gtest.h>

// ------------------------------------------------------------------------------------------------------------
// -------------TESTS FOR PCG-------------------------------------------------
// ------------------------------------------------------------------------------------------------------------

// test the default sequence is the expected one
TEST(PCGTest, test_default_sequence) {
  PCG pcg = PCG();

  EXPECT_EQ(pcg.state, 1753877967969059832ull);
  EXPECT_EQ(pcg.inc, 109ull);

  uint32_t expected[] = {2707161783u, 2068313097u, 3122475824u, 2211639955u, 3215226955u, 3421331566u};

  for (auto ran : expected) {
    EXPECT_EQ(ran, pcg.random());
  }
}

